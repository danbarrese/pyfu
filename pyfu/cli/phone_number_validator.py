def parse_with_no_region(num):
    return phonenumbers.parse(num, None)

def parse_with_no_region_and_prepend_plus(num):
    return phonenumbers.parse('+' + num, None)

def parse_with_region(num, region='US'):
    with_plus = num
    if not num.startswith('+'):
        with_plus = '+' + num
    return phonenumbers.parse(num, region)

def parse(num):
    try:
        parsed = parse_with_region(num)
        if not phonenumbers.is_valid_number(parsed):
            raise Exception
        return parsed
    except Exception as e:
        pass

    try:
        parsed = parse_with_no_region(num)
        if not phonenumbers.is_valid_number(parsed):
            raise Exception
        return parsed
    except Exception as e:
        pass

    try:
        parsed = parse_with_no_region_and_prepend_plus(num)
        if not phonenumbers.is_valid_number(parsed):
            raise Exception
        return parsed
    except Exception as e:
        pass

    return None


